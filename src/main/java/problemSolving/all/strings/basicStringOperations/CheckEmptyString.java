package problemSolving.all.strings.basicStringOperations;

public class CheckEmptyString {
    public static void main(String[] args) {
        String str = "";

        if (str.isEmpty()) {
            System.out.println("String is empty.");
        } else {
            System.out.println("String is not empty.");
        }


        // OR


        String str1 = "";

        if (str1.length() == 0) {
            System.out.println("String is empty.");
        } else {
            System.out.println("String is not empty.");
        }
    }





}
